<#
.SYNOPSIS
    A sample tool for testing metadata support.

.DESCRIPTION
    This is a test executable with metadata provided via a metadata.*.ps1 file.
    The metadata file allows executables to have PowerShell help documentation.

.PARAMETER Config
    Path to the configuration file.

.PARAMETER Verbose
    Enable verbose output.

.EXAMPLE
    mytool --config config.json

    Runs the tool with the specified configuration.

.EXAMPLE
    mytool --verbose

    Runs the tool with verbose output enabled.
#>
